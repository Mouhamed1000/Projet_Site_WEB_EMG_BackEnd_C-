public class LoginModel
{
    //Définition des propriétés du formulaire de Connexion
    private String Email;
    private String Password;

    //Définition des getters et setters
    public String _email
    {
        get { return Email; }
        set { Email = value; }
    }

    public String _password
    {
        get { return Password; }
        set { Password = value; }
    }
}